  <h2>Quiénes Somos</h2>
  <p>La Fundación Guaraní fue creada en 2010 con el propósito de promover el desarrollo social y cultural en nuestra comunidad, trabajando especialmente con poblaciones vulnerables para mejorar su calidad de vida.</p>
  <p>Esta iniciativa surgió a partir de un grupo de ciudadanos comprometidos con la inclusión y el respeto por nuestras raíces guaraníes, buscando fortalecer la identidad cultural y generar oportunidades reales para todos.</p>

